import { readFile, writeFile } from "node:fs/promises";
import { basename, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const checkOnly = process.argv.includes("--check");

const ALLOWED_FILES = [
  "custom-direct-domain.list",
  "custom-proxy-domain.list",
  "pt-direct-domain.list",
  "misc-direct-domain.list",
  "android-fcm-domain.list",
  "android-google-play-domain.list",
  "japan-services-domain.list",
  "fake-ip-filter-domain.list",
  "fake-ip-filter-android-domain.list",
  "fake-ip-filter-tailnet-domain.list",
];

const outputPath = resolve(rootDir, "src/core/default-rule-bodies.js");

async function main() {
  const entries = {};
  for (const filename of ALLOWED_FILES) {
    const filePath = resolve(rootDir, "rules", filename);
    try {
      const content = await readFile(filePath, "utf8");
      entries[filename] = content;
    } catch (error) {
      if (error.code === "ENOENT") {
        console.error(`Rule file not found: ${filename}`);
        process.exit(1);
      }
      throw error;
    }
  }

  const lines = [
    "// This file is generated by scripts/generate-rule-bodies.mjs.",
    "// Edit rules/*.list files instead.",
    "",
    "const bodies = {",
  ];

  for (const [filename, content] of Object.entries(entries)) {
    lines.push(`  ${JSON.stringify(filename)}: ${JSON.stringify(content)},`);
  }

  lines.push("};");
  lines.push("");
  lines.push("export function getRuleBody(filename) {");
  lines.push("  return bodies[filename] ?? null;");
  lines.push("}");
  lines.push("");
  lines.push("export function listRuleFiles() {");
  lines.push("  return Object.keys(bodies);");
  lines.push("}");
  lines.push("");

  const output = lines.join("\n");

  if (checkOnly) {
    let current = "";
    try {
      current = await readFile(outputPath, "utf8");
    } catch (error) {
      if (error.code !== "ENOENT") throw error;
    }
    if (current !== output) {
      console.error(`${basename(outputPath)} is out of sync. Run npm run rules:generate.`);
      process.exit(1);
    }
    console.log("default-rule-bodies.js is up to date.");
  } else {
    await writeFile(outputPath, output);
    console.log(`Generated ${outputPath}`);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
